#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t		i;
	size_t		len;
	size_t		j;

	len = ft_strlen(s1) + ft_strlen(s2);
	j = 0;
	while (s1[j])
		j++;
	i = 0;
	while (s2[i])
	{
		s1[j] = s2[i];
		i++;
		j++;
	}
	s1[j] = '\0';
	return (s1);
}
